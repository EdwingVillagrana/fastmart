    private Proveedor consultarProveedor(String nombre) {
        try {
            Proveedor proveedor = proveedoresNegocio.consultarPorNombre(nombre);
            if (proveedor == null) {
                JOptionPane.showMessageDialog(null, "El proveedor no existe.", "Error", JOptionPane.INFORMATION_MESSAGE);
            } else {
                return proveedor;
            }
        } catch (NegocioException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        return null;
    }

    private Categoria consultarCategoria(String nombre) {
        try {
            Categoria categoria = categoriasNegocio.consultarPorNombre(nombre);
            if (categoria == null) {
                JOptionPane.showMessageDialog(null, "La categoria no existe.", "Error", JOptionPane.INFORMATION_MESSAGE);
            } else {
                return categoria;
            }
        } catch (NegocioException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        return null;
    }

    private void listarProveedores() {
        try {
            this.listaProveedores = proveedoresNegocio.consultarTodos();

            for (Proveedor proveedor : listaProveedores) {
                modeloProveedores.addElement(proveedor.getNombre());
            }
        } catch (NegocioException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void listarCategorias() {
        try {
            this.listaCategorias = categoriasNegocio.consultarTodos();

            for (Categoria categoria : listaCategorias) {
                modeloCategorias.addElement(categoria.getNombre());
            }
        } catch (NegocioException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void limpiarCampos() {
        this.txtNombre.setText("");
        this.txtCompra.setText("");
        this.txtVenta.setText("");
        this.comboCategoria.setSelectedIndex(0);
        this.comboProveedor.setSelectedIndex(0);
        this.txtCodigo.setText("");
        
    }

    private boolean validarCampos() {
        if (this.txtNombre.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Compleme el campo nombre", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return false;
        } else if (this.txtCompra.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Compleme el campo precio compra", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return false;
        } else if (this.txtVenta.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Compleme el campo precio venta", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return false;
        } else if (this.comboCategoria.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Seleccione la categoria", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return false;
        } else if (this.comboProveedor.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Seleccione el proveedor", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        return true;
    }

